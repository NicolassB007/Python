# Operadores aritméticos

produto = 10
produto2 = 20

soma = produto + produto2
subtracao = produto - produto2
multiplicacao = produto * produto2
divisao = produto / produto2
divisao_inteiro = produto // produto2
exponenciacao = produto ** produto2
resto_da_divisao = produto % produto2
print(f"SOMA = {soma}")
print(f"SUBTRAÇÃO = {subtracao}")
print(f"MULTIPLICAÇÃO = {multiplicacao}")
print(f"DIVISÃO = {divisao}")
print(f"DIVISÃO INTEIRA = {divisao_inteiro}")
print(f"EXPONENCIAÇÃO = {exponenciacao}")
print(f"RESTO DA DIVISÃO (Módulo) = {resto_da_divisao}")

# Fazendo uma expressão

