# Operadores de atribuição
# São utilizados para definir ou sobrescrever o valor de uma
# variável

# Atribuição simples
print("Atribuição simples")
saldo = 500
print(saldo)

# Sobrescrendo
print("Sobrescrendo")
saldo = 200
print(saldo)

# Atribuição com adição
print("Atribuição com adição")
saldo += 600
print(saldo)

# Atribuição com subtração
print("Atribuição com subtração")
saldo -= 400
print(saldo)

# Atribuição com multiplicação
print("Atribuição com multiplicação")
saldo *= 5
print(saldo)

# Atribuição com divisão
print("Atribuição com divisão")
saldo /= 8
print(saldo)

# Atribuição com divisão inteira
print("Atribuição com divisão inteira")
saldo //= 3.7
print(saldo)

# Atribuição com exponenciação
print("Atribuição com exponenciação")
saldo **= 1
print(saldo)

# Atribuição com módulo (Resto da divisão)
print("Atribuição com módulo (Resto da divisão)")
saldo %= 2
print(saldo)