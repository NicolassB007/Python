# Métodos úteis para Dicionários
# .clear() -> Limpa o dicionário
# .copy() -> Tira uma cópia do dicionário
# .fromkeys()
# .get() -> Uma segunda forma de acessar os valores do nosso dicionário
# .items() -> Retorna uma lista de tuplas 
# .keys() -> Retorna as chaves do dicionário
# .pop() -> Remove a chave informada, e se a chave for encontrada ela remove e informa o valor removido
# .popitem() -> Não é preciso informar a chave, ele vai removendo as chaves
# .setdefault()
# .update() 
# .values() -> Retorna os valores do dicionário
# .in() -> Verifica se uma chave já existe no dicionário
# .del() -> Outra forma de tirar um valor do dicionário